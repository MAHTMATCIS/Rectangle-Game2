from tkinter import *
from tkinter .ttk import *
from contact import *

class Setting(Tk):
    def __init__(self,*args,**kwargs):
        super().__init__(*args,**kwargs)
        self.title('Setting to '+title)
        width=self.winfo_screenwidth()
        height=self.winfo_screenheight()
        wid=600
        hei=400
        self.geometry(f'{wid}x{hei}+{width//2-wid//2}+{height//2-hei//2}')
        self.focus_set()
        self.mainloop()